#!/usr/bin/env bash
#
# silverblue-autoinstall.sh — unattended Arch Silverblue install onto /dev/vda.
#
# Runs in the live ISO, gated by the QEMU fw_cfg blob opt/silverblue/scenario=install (see
# silverblue-autoinstall.service). It partitions the disk (ESP + Btrfs), creates the initial
# root-<TS> and @home subvolumes, pacstraps a minimal system + the Silverblue tools, installs
# the requested bootloader with an initial boot entry, configures serial-console autologin so
# the QEMU harness can drive the booted system, then powers off. Progress is reported with
# SILVERBLUE-* markers on the serial console.
#
# It is intentionally conservative: it only proceeds when the scenario blob says "install".
set -euo pipefail

marker() { printf '%s\n' "$*"; }
fwc() { cat "/sys/firmware/qemu_fw_cfg/by_name/$1/raw" 2>/dev/null || true; }

install_sdboot() {
    local mnt=$1 snap=$2 uuid=$3
    local efi="$mnt/efi"
    bootctl --esp-path="$efi" install
    mkdir -p "$efi/EFI/BOOT" "$efi/silverblue/$snap" "$efi/loader/entries"
    # Removable fallback so the VM boots even without persisted EFI NVRAM.
    cp "$efi/EFI/systemd/systemd-bootx64.efi" "$efi/EFI/BOOT/BOOTX64.EFI"
    cp "$mnt/boot/vmlinuz-linux" "$efi/silverblue/$snap/"
    cp "$mnt/boot/initramfs-linux.img" "$efi/silverblue/$snap/"
    # No explicit default= : systemd-boot then selects the newest-version entry, which is how
    # silverblue-update makes a freshly registered root boot next without touching the default.
    cat > "$efi/loader/loader.conf" <<EOF
timeout 3
console-mode max
EOF
    cat > "$efi/loader/entries/$snap.conf" <<EOF
title    Arch Silverblue (initial) $snap
sort-key silverblue
version  ${snap#root-}
linux    /silverblue/$snap/vmlinuz-linux
initrd   /silverblue/$snap/initramfs-linux.img
options  root=UUID=$uuid rootflags=subvol=$snap rootfstype=btrfs rw console=ttyS0,115200 console=tty0
EOF
}

install_grub() {
    local mnt=$1 snap=$2 uuid=$3
    local efi="$mnt/efi"
    arch-chroot "$mnt" pacman -S --noconfirm grub
    arch-chroot "$mnt" grub-install --target=x86_64-efi --efi-directory=/efi \
        --boot-directory=/efi --bootloader-id=GRUB --removable
    mkdir -p "$efi/grub"
    grub-editenv "$efi/grub/grubenv" create
    grub-editenv "$efi/grub/grubenv" set "saved_entry=$snap"
    cat > "$efi/grub/grub.cfg" <<EOF
# Generated by silverblue-autoinstall; managed by silverblue-update thereafter.
load_env --file \${prefix}/grubenv
set default="\${saved_entry}"
if [ -n "\${next_entry}" ]; then
    set default="\${next_entry}"; set next_entry=; save_env --file \${prefix}/grubenv next_entry
    set recordfail=1; save_env --file \${prefix}/grubenv recordfail
fi
if [ "\${recordfail}" = 1 ]; then set timeout=-1; else set timeout=3; fi
menuentry 'Arch Silverblue (initial) $snap' --id $snap {
    insmod btrfs
    insmod part_gpt
    search --no-floppy --fs-uuid --set=root $uuid
    linux  /$snap/boot/vmlinuz-linux root=UUID=$uuid rootflags=subvol=$snap rootfstype=btrfs rw console=ttyS0,115200 console=tty0
    initrd /$snap/boot/initramfs-linux.img
}
EOF
}

configure_pacman_repo() {
    local mnt=$1 net=$2
    cat >> "$mnt/etc/pacman.conf" <<'EOF'

[silverblue-local]
SigLevel = Optional TrustAll
Server = file:///opt/silverblue/localrepo
EOF
    if [[ "$net" != 1 ]]; then
        # Hermetic update test: disable remote repos so `pacman -Syu` only needs the offline
        # file:// repo. Comment each remote section header and its Include/Server lines.
        awk '
            /^\[(core|extra|multilib)\]/ { print "#" $0; skip=1; next }
            skip==1 && /^(Include|Server)/ { print "#" $0; next }
            /^\[/ { skip=0 }
            { print }
        ' "$mnt/etc/pacman.conf" > "$mnt/etc/pacman.conf.new"
        mv "$mnt/etc/pacman.conf.new" "$mnt/etc/pacman.conf"
    fi
}

main() {
    local scenario net bootloader disk esp rootpart ts snap pool_uuid
    # Config comes from the environment (the QEMU harness drives this over the autologin
    # shell), falling back to QEMU fw_cfg blobs for the service-driven path.
    scenario=${SB_SCENARIO:-$(fwc opt/silverblue/scenario)}
    if [[ "$scenario" != install ]]; then
        marker "SILVERBLUE-INSTALL-SKIP scenario=${scenario:-none}"
        exit 0
    fi
    net=${SB_NET:-$(fwc opt/silverblue/net)}; net=${net:-0}
    bootloader=${SB_BOOTLOADER:-$(fwc opt/silverblue/bootloader)}; bootloader=${bootloader:-systemd-boot}
    disk=/dev/vda

    # shellcheck disable=SC2154   # LINENO is provided by bash
    trap 'marker "SILVERBLUE-INSTALL-FAIL line=$LINENO"; sync; poweroff -f' ERR

    ts=$(date +%Y%m%d-%H%M%S)
    snap="root-$ts"
    marker "SILVERBLUE-INSTALL-START disk=$disk snap=$snap bootloader=$bootloader net=$net"

    # --- Partition: p1 ESP (FAT32), p2 Btrfs ---------------------------------------------
    wipefs -a "$disk"
    sgdisk -Z "$disk"
    sgdisk -n1:0:+512M -t1:ef00 -c1:EFI "$disk"
    sgdisk -n2:0:0     -t2:8300 -c2:silverblue "$disk"
    partprobe "$disk" 2>/dev/null || true
    udevadm settle || true
    sleep 1
    esp="${disk}1"
    rootpart="${disk}2"

    mkfs.fat -F32 -n SB-ESP "$esp"
    mkfs.btrfs -f -L silverblue "$rootpart"

    # --- Subvolumes ----------------------------------------------------------------------
    mount "$rootpart" /mnt
    btrfs subvolume create "/mnt/$snap"
    btrfs subvolume create /mnt/@home
    umount /mnt

    mount -o "subvol=$snap,compress=zstd" "$rootpart" /mnt
    mkdir -p /mnt/efi /mnt/home
    mount -o subvol=@home "$rootpart" /mnt/home
    mount "$esp" /mnt/efi

    # --- Wait for outbound network (pacstrap needs the mirrors) --------------------------
    marker "SILVERBLUE-INSTALL-NETWAIT"
    local _
    for _ in $(seq 1 30); do
        if curl -fsS --max-time 5 https://geo.mirror.pkgbuild.com/ >/dev/null 2>&1 \
            || ping -c1 -W2 8.8.8.8 >/dev/null 2>&1; then
            break
        fi
        sleep 2
    done

    # --- Base system (no linux-firmware: virtio needs none, and it saves ~700MB) ---------
    pacstrap -K /mnt base linux mkinitcpio btrfs-progs \
        arch-install-scripts dosfstools gptfdisk efibootmgr

    # fstab: omit the '/' line — the root subvolume comes from the kernel cmdline rootflags.
    genfstab -U /mnt | awk '$2 != "/"' > /mnt/etc/fstab

    echo silverblue > /mnt/etc/hostname
    ln -sf /usr/share/zoneinfo/UTC /mnt/etc/localtime
    arch-chroot /mnt passwd -d root

    # initramfs must carry btrfs (autodetect can't see it from the live medium's root).
    sed -i 's/^MODULES=.*/MODULES=(btrfs)/' /mnt/etc/mkinitcpio.conf
    arch-chroot /mnt mkinitcpio -P

    # Serial-console autologin on the target so the harness can drive it.
    install -Dm0644 /dev/stdin \
        /mnt/etc/systemd/system/serial-getty@ttyS0.service.d/autologin.conf <<'EOF'
[Service]
ExecStart=
ExecStart=-/sbin/agetty -o '-p -- \\u' --autologin root --keep-baud 115200,38400,9600 - $TERM
EOF

    # --- Silverblue tools into the target ------------------------------------------------
    install -Dm0755 /usr/bin/silverblue-update /mnt/usr/bin/silverblue-update
    mkdir -p /mnt/usr/lib/silverblue
    cp -a /usr/lib/silverblue/. /mnt/usr/lib/silverblue/
    # Ensure the entry-point scripts are executable on the target regardless of source modes.
    chmod 0755 /mnt/usr/lib/silverblue/silverblue-mark-good.sh \
        /mnt/usr/lib/silverblue/silverblue-rollback.sh
    local u
    for u in silverblue-mark-good.service silverblue-rollback.service silverblue-rollback.target; do
        install -Dm0644 "/usr/lib/systemd/system/$u" "/mnt/usr/lib/systemd/system/$u"
    done
    install -Dm0644 /etc/systemd/system.conf.d/silverblue-watchdog.conf \
        /mnt/etc/systemd/system.conf.d/silverblue-watchdog.conf
    arch-chroot /mnt systemctl enable silverblue-mark-good.service

    # --- Local repo + marker v1; configure pacman for the update test --------------------
    cp -a /opt/silverblue /mnt/opt/silverblue
    arch-chroot /mnt pacman -U --noconfirm \
        /opt/silverblue/install/silverblue-marker-1-1-any.pkg.tar.zst
    configure_pacman_repo /mnt "$net"

    # --- Bootloader ----------------------------------------------------------------------
    pool_uuid=$(blkid -s UUID -o value "$rootpart")
    if [[ "$bootloader" == grub ]]; then
        install_grub /mnt "$snap" "$pool_uuid"
    else
        install_sdboot /mnt "$snap" "$pool_uuid"
    fi

    sync
    umount -R /mnt
    marker "SILVERBLUE-INSTALL-OK snap=$snap uuid=$pool_uuid bootloader=$bootloader"
    poweroff -f
}

main "$@"
